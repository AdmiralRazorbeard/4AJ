			<h1>L'association</h1>
			<p>
				L'association "4AJ, un tremplin pour les jeunes", a été créer le 7 juin 2010, suite à la fusion des 3 F.J.T. arragesois : Anne Frank, Clair Logis et Nobel.<br />
				Plus de 20 000 jeunes ont été accueillis et logés depuis un demi-siècle dans les 3 établissements.<br />
				Cette fusion a part ailleurs la mutualisation des moyens, un partage des compétences, et la mise en oeuvre d'actions communes.<br />
				Cette association est active dans les parcours des usagers, des jeunes. Elle agit dans le parcours de soins à travers une organisation de coopération. <br />
				Un FJT est une structure qui propose un logement peu onéreux à des jeunes, afin que ce soit un tremplin pour une insertion sociale et professionnelle, la mission première est l'insertion sociale et professionnelle des jeunes par le logement. <br />
				Dans le Pas-de-Calais, les FJT sont atypiques car 40% des jeunes accueillis sont des jeunes confiés dans le cadre de l'aide sociale à l'enfance. <br />
				Aujourd'hui ce sont 160 places dans les trois établissements dont 39 places de centre d'hébergement en réinsertion sociale (ou CHRS), 33 places dans le cadre de l'aide sociale à l'enfance, 5 places d'urgence, et 8 places dans le cadre de l'implication dans la campagne hivernale. L'originalité, la transversalité des dispositifs, la non stigmatisation du public accueilli sont mises en exergue par l'accueil d'une jeunesse large âgée de 16 à 30 ans.
